package com.basesoft.modules.contract;

public class Contract {

	String id;
	String code;
	String subject;
	String bdepart;
	String pjcode;
	String amount;
	String stage1;
	String stage2;
	String stage3;
	String status;
	
	public String getId() {
		return id;
	}
	
	public void setId(String id) {
		this.id = id;
	}
	
	public String getCode() {
		return code;
	}
	
	public void setCode(String code) {
		this.code = code;
	}
	
	public String getSubject() {
		return subject;
	}
	
	public void setSubject(String subject) {
		this.subject = subject;
	}
	
	public String getBdepart() {
		return bdepart;
	}
	
	public void setBdepart(String bdepart) {
		this.bdepart = bdepart;
	}
	
	public String getPjcode() {
		return pjcode;
	}
	
	public void setPjcode(String pjcode) {
		this.pjcode = pjcode;
	}
	
	public String getAmount() {
		return amount;
	}
	
	public void setAmount(String amount) {
		this.amount = amount;
	}
	
	public String getStage1() {
		return stage1;
	}
	
	public void setStage1(String stage1) {
		this.stage1 = stage1;
	}
	
	public String getStage2() {
		return stage2;
	}
	
	public void setStage2(String stage2) {
		this.stage2 = stage2;
	}
	
	public String getStage3() {
		return stage3;
	}
	
	public void setStage3(String stage3) {
		this.stage3 = stage3;
	}
	
	public String getStatus() {
		return status;
	}
	
	public void setStatus(String status) {
		this.status = status;
	}
}
