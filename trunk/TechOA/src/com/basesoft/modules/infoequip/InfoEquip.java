package com.basesoft.modules.infoequip;

public class InfoEquip {

	public String id;
	public String code;
	public String name;
	public String model;
	public String buydate;
	public String producedate;
	public float buycost;
	public float nowcost;
	public int life;
	public String status;
	public String empcode;
	public String lenddate;
	
	public String getId() {
		return id;
	}
	
	public void setId(String id) {
		this.id = id;
	}
	
	public String getCode() {
		return code;
	}
	
	public void setCode(String code) {
		this.code = code;
	}
	
	public String getName() {
		return name;
	}
	
	public void setName(String name) {
		this.name = name;
	}
	
	public String getModel() {
		return model;
	}
	
	public void setModel(String model) {
		this.model = model;
	}
	
	public String getBuydate() {
		return buydate;
	}
	
	public void setBuydate(String buydate) {
		this.buydate = buydate;
	}
	
	public String getProducedate() {
		return producedate;
	}
	
	public void setProducedate(String producedate) {
		this.producedate = producedate;
	}
	
	public float getBuycost() {
		return buycost;
	}
	
	public void setBuycost(float buycost) {
		this.buycost = buycost;
	}
	
	public float getNowcost() {
		return nowcost;
	}
	
	public void setNowcost(float nowcost) {
		this.nowcost = nowcost;
	}
	
	public int getLife() {
		return life;
	}
	
	public void setLife(int life) {
		this.life = life;
	}
	
	public String getStatus() {
		return status;
	}
	
	public void setStatus(String status) {
		this.status = status;
	}
	
	public String getEmpcode() {
		return empcode;
	}
	
	public void setEmpcode(String empcode) {
		this.empcode = empcode;
	}
	
	public String getLenddate() {
		return lenddate;
	}
	
	public void setLenddate(String lenddate) {
		this.lenddate = lenddate;
	}
}
