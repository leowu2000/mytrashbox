package com.basesoft.modules.visit;

public class Visit {

	String v_empcode;
	String v_ip;
	String type;
	String status;
	
	public String getV_empcode() {
		return v_empcode;
	}
	
	public void setV_empcode(String v_empcode) {
		this.v_empcode = v_empcode;
	}
	
	public String getV_ip() {
		return v_ip;
	}
	
	public void setV_ip(String v_ip) {
		this.v_ip = v_ip;
	}
	
	public String getType() {
		return type;
	}
	
	public void setType(String type) {
		this.type = type;
	}
	
	public String getStatus() {
		return status;
	}
	
	public void setStatus(String status) {
		this.status = status;
	}
}
