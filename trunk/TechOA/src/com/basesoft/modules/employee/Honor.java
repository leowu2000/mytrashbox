package com.basesoft.modules.employee;

public class Honor {
	String id;
	String empcode;
	String h_year;
	String h_name;
	String h_describe;
	
	public String getId() {
		return id;
	}
	
	public void setId(String id) {
		this.id = id;
	}
	
	public String getEmpcode() {
		return empcode;
	}
	
	public void setEmpcode(String empcode) {
		this.empcode = empcode;
	}
	
	public String getH_year() {
		return h_year;
	}
	
	public void setH_year(String h_year) {
		this.h_year = h_year;
	}
	
	public String getH_name() {
		return h_name;
	}
	
	public void setH_name(String h_name) {
		this.h_name = h_name;
	}
	
	public String getH_describe() {
		return h_describe;
	}
	
	public void setH_describe(String h_describe) {
		this.h_describe = h_describe;
	}
}